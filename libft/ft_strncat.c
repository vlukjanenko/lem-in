#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*tmp;
	size_t	len;

	tmp = s1;
	len = 0;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0' && len < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		len++;
	}
	*s1 = '\0';
	return (tmp);
}
