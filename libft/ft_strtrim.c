#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	n;

	if (!s)
		return (NULL);
	n = ft_strlen(s);
	while ((s[n - 1] == ' ' || s[n - 1] == '\n' || s[n - 1] == '\t') && n >= 1)
	{
		n--;
	}
	while ((*s == ' ' || *s == '\n' || *s == '\t') && n >= 1)
	{
		s++;
		n--;
	}
	return (ft_strsub(s, 0, n));
}
